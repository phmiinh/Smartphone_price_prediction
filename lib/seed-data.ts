import type { Product, PriceRange, ProductSpecs } from "./types"

type SpecInput = {
  ramGb: number
  romGb: number
  battery: number
  screen: number
  resolution: [number, number]
  primaryMp: number
  frontMp: number
  cores: number
  clock: number
  weight: number
  depth: number
  talk: number
  blue?: 0 | 1
  dualSim?: 0 | 1
  fourG?: 0 | 1
  threeG?: 0 | 1
  touch?: 0 | 1
  wifi?: 0 | 1
}

const buildSpecs = ({
  ramGb,
  romGb,
  battery,
  screen,
  resolution,
  primaryMp,
  frontMp,
  cores,
  clock,
  weight,
  depth,
  talk,
  blue = 1,
  dualSim = 1,
  fourG = 1,
  threeG = 1,
  touch = 1,
  wifi = 1,
}: SpecInput): ProductSpecs => ({
  ram: ramGb * 1024,
  int_memory: romGb,
  battery_power: battery,
  px_width: resolution[0],
  px_height: resolution[1],
  sc_w: screen,
  sc_h: screen,
  pc: primaryMp,
  fc: frontMp,
  n_cores: cores,
  clock_speed: clock,
  mobile_wt: weight,
  m_dep: depth,
  talk_time: talk,
  blue,
  dual_sim: dualSim,
  four_g: fourG,
  three_g: threeG,
  touch_screen: touch,
  wifi,
})

const determinePriceRange = (price: number): PriceRange => {
  if (price < 4_000_000) return 0
  if (price < 8_000_000) return 1
  if (price < 15_000_000) return 2
  return 3
}

type ProductSeed = Omit<Product, "price_range"> & { price_range?: PriceRange }

const createProduct = (seed: ProductSeed): Product => ({
  ...seed,
  price_range: seed.price_range ?? determinePriceRange(seed.price),
})

